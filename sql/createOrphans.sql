DROP TABLE FILES_ORPHANS;

CREATE TABLE FILES_ORPHANS (
   FILENAME          VARCHAR2(1000) UNIQUE NOT NULL
  ,DTIME             TIMESTAMP
  ,Host              VARCHAR2(1000)
  ,Status            Number(10)
  ,COMMENT_STR       VARCHAR2(1000)
  ,CONSTRAINT FK_OR FOREIGN KEY (FILENAME)
   REFERENCES FILES_CREATED (FILENAME) ON DELETE CASCADE
);
				       
